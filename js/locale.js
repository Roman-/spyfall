function tr(w) {
    if (!Glob.localeMap.hasOwnProperty(w)) {
        console.warn("no locale thing for " + w);
        return w;
    }
    var options = Glob.localeMap[w];
    if (Glob.currentLocale === 'ru' && options.hasOwnProperty(Glob.currentLocale)) {
        return options[Glob.currentLocale];
    }
    // else consider the word in Dutch
    if (Glob.currentLocale === 'nl' && options.hasOwnProperty('nl')) {
        return options['nl'];
    }
    // else consider the word in English
    if (options.hasOwnProperty('en'))
        return options['en'];

    return options['en'];
}
